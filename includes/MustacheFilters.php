<?php

namespace MediaWiki\Extension\Mustache;

use MediaWiki\Parser\Sanitizer;

class MustacheFilters {

	public static function getBuiltinFilters(): array {
		return [
			'css-selector' => static function ( $value ): string {
				return preg_replace( '/[^a-zA-Z0-9_.#-]/', '', (string)$value );
			},
			'css-value' => static function ( $value ): string {
				$value = preg_replace( '|[\'":;{}\\\\/<>]|', '', (string)$value );
				return Sanitizer::checkCss( (string)$value );
			},
			'js-string' => static function ( $value ): string {
				$json = json_encode( $value, JSON_HEX_TAG | JSON_HEX_APOS | JSON_HEX_QUOT | JSON_HEX_AMP | JSON_UNESCAPED_UNICODE );
				return substr( $json, 1, -1 );
			},
			'js-identifier' => static function ( $value ): string {
				return preg_replace( '/[^a-zA-Z0-9_$]/', '', (string)$value );
			},
			'url' => static function ( $value ): string {
				// Fix urls generated by {{filepath
				if ( str_starts_with( $value, '//' ) ) {
					$value = 'https:' . $value;
				}
				$url = filter_var( $value, FILTER_VALIDATE_URL );
				if ( $url === false ) {
					return '';
				}
				$parsed = parse_url( $url );
				$scheme = $parsed['scheme'] ?? '';
				if ( $scheme !== 'http' && $scheme !== 'https' ) {
					return '';
				}
				return $url;
			},
			'attribute' => static function ( $value ): string {
				$escaped = htmlspecialchars( $value, ENT_QUOTES | ENT_SUBSTITUTE, 'UTF-8' );
				return str_replace( '=', '&#x3D;', $escaped );
			},
		];
	}

	public static function parseInterpolations( string $text ): array {
		$result = [];
		preg_match_all( '/\{\{([^}]+)}}/', $text, $matches );
		foreach ( $matches[1] as $expr ) {
			$filters = array_map( 'trim', explode( '|', $expr ) );
			$varName = array_shift( $filters );
			$result[] = [
				$varName,
				$filters
			];
		}
		return $result;
	}
}
